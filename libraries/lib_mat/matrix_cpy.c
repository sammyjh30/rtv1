#include "lib_mat.h"

void	matrix_cpy(t_mat *source, t_mat *dest)
{
	int i;
	int j;

	i = 0;
	while (i < 4)
	{
		j = 0;
		while (j < 4)
		{
			dest->mat[i][j] = source->mat[i][j];
			j++;
		}
		i++;
	}
}
